# Atribuições
saldo = 200

print(saldo)

# Atribuição por Soma
saldo += 10
print(saldo)

# Atribuição por Diferença
saldo -= 5
print(saldo)

# Atribuição por Divisão Inteira
saldo //= 2
print(saldo)

# Atribuição por Divisão Float
saldo /= 2
print(saldo)

# Atribuição por Multiplicação
saldo *= 10
print(saldo)

# Atribuição por Módulo (Resto da Divisão)
saldo %= 4
print(saldo)

# Atribuição por Exponenciação
saldo **= 2
print(saldo)
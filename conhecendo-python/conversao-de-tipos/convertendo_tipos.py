# Convertendo de Float para Inteiro

numero = 10.5
print(int(numero))

# Convertendo string para inteiro
print(int("1020"))

# Convertendo string para float
print(float("10.20"))

# Convertendo String para Número
print((str(15)))

# Descobrindo o tipo
print(type(str(15)))

# Conversão por divisão
print(100/2) # Retorna valor tipo float, usando uma barra 
print(100 // 2) # Retorna valor tipo int, usando duas barra
